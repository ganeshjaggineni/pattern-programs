#include<stdio.h>
int main()
{
int n,rows,cols;
printf("\nEnter a number :");
scanf("%d",&n);
//first half
for(rows=1;rows<=n;rows++)
{
for(cols=1;cols<=rows;cols++)
{
printf("*");
}
printf("\n");
}
//second half
for(rows=1;rows<=2*n-1;rows++)
{
for(cols=1;cols<=n-rows;cols++)
{
printf("*");
}
printf("\n");
}
return 0;
}
